/**
Classe utilizada para demonstrar o uso do operador Lógico OU ( || )
**/

public class OperadorLogicoou {
	public static void main (String [] args) {
		boolean a = true;
		boolean b = false;
		boolean c = false;
		
		System.out.println(a || b);
		System.out.println(b || c);
	}
}

/**
O operador ||, é chamado de “operador OU”. Ele retornará true se ao menos um dos dois operadores lógicos forem verdadeiros, logo, temos <operador1> || <operador2>.
**/